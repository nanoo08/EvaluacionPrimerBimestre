package evPrimerBimestre;

import java.util.Scanner;

public class ejercicio16 {

	public static void main(String[] args) {
		Scanner ingreso = new Scanner (System.in);
	
		int variable;
		
		System.out.println("Ingrese un numero dependiendo el cafe que quiera:");
		variable = ingreso.nextInt();
		
		switch (variable) {
		
		case 1 :System.out.println("cafe cortado");
		break;
		
		case 2 :System.out.println("cafe latte");
		break;
		
		case 3 :System.out.println("cafe solo");
		break;
		
		case 4 :System.out.println("cafe lagrima");
		break;
		
		case 5 :System.out.println("salio del programa");
		break;
		
		
		
		
		
		
		
		
		
		
}		
}
}